<script>
    function haut_de_page() {
        $('html,body').animate({
            scrollTop: 0
        }, 'slow');
    }

    $(function() {
        $(window).scroll(() => {
            if ($(this).scrollTop() > $('#bottom_header').offset().top) {
                document.getElementById(`retour_top`).style.display = "block";
            } else {
                document.getElementById(`retour_top`).style.display = "none";
            }
        });
    });
</script>